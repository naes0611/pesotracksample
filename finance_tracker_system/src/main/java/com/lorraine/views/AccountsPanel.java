    private void onDeleteAccount(AccountModel account) {
        boolean success = accountService.deleteAccount(account.getID());
        if (success) {
            loadAccounts();
            Notifications.getInstance().show(Notifications.Type.SUCCESS, "Account deleted.");
        } else {
            Notifications.getInstance().show(Notifications.Type.ERROR, "Failed to delete account.");
        }
    }
    
