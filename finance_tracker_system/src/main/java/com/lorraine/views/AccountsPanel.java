    private void applySummaryStyle(JPanel card, JLabel label, JLabel value, Color background, Color foreground) {
        card.setBackground(background);
        card.putClientProperty(FlatClientProperties.STYLE, "arc:14;");

        label.setFont(new Font("Segoe UI", Font.BOLD, 11));
        label.setForeground(new Color(foreground.getRed(), foreground.getGreen(), foreground.getBlue(), 180));

        value.setFont(new Font("Segoe UI Semibold", Font.PLAIN, 22));
        value.setForeground(foreground);
    }
    
    private void loadAccounts() {
        cardsContainer.removeAll();

        try {
            List<AccountModel> accounts = accountService.getAccountsByUser(Session.getUserID());

            BigDecimal totalBalance = accounts.stream()
                .map(AccountModel::getBalance)
                .reduce(BigDecimal.ZERO, BigDecimal::add);

            totalAccountsValue.setText(String.valueOf(accounts.size()));
            totalBalanceValue.setText(CurrencyFormatter.format(totalBalance));

            for (AccountModel account : accounts) {
                AccountCard card = new AccountCard(account);
                card.setOnEdit(this::onEditAccount);
                card.setOnDelete(this::onDeleteAccount);
                cardsContainer.add(card);
            }
        } catch (Exception e) {
            LOGGER.log(Level.SEVERE, "Error loading accounts", e);
        }

        cardsContainer.revalidate();
        cardsContainer.repaint();
    }
    
